//What is Data Binding?
//allows us to communicate between a component class and its corresponding view template
//in the component class we specify the UI logic - properties and methods which we can use
//in the view template of that component

//one-way data binding
// from component class to view template or vice versa

//two-way data binding
//from component class to view template and view template to component class
// requires a combination of property binding and event binding
